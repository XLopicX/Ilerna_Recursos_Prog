public class DeclaracionVariables {
    public static void main(String[] args) {
        int numero = 0;
        char letra = 'a';
        String palabra = "hola";
        boolean booleano = true;

        System.out.println(numero);
        System.out.println(letra);
        System.out.println(palabra);
        System.out.println(booleano);

        numero = 10;
        letra = 'b';
        palabra = "adios";
        booleano = false;

        System.out.println(numero);
        System.out.println(letra);
        System.out.println(palabra);
        System.out.println(booleano);
    }
}
